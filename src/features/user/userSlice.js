import { createAsyncThunk, createSlice } from "@reduxjs/toolkit";
import {
  UpdateUser,
  fetcLoggedInUserInfo,
  fetcLoggedInUserOrders,
} from "./userAPI";

const initialState = {
  userInfo: null,
  userOrders: [],
  status: "idle",
};
export const fetcLoggedInUserOrdersAsync = createAsyncThunk(
  "user/fetcLoggedInUserOrders",
  async (userId) => {
    const response = await fetcLoggedInUserOrders(userId);
    return response.data;
  }
);
export const fetcLoggedInUserInfoAsync = createAsyncThunk(
  "user/fetcLoggedInUserInfo",
  async (userId) => {
    const response = await fetcLoggedInUserInfo(userId);
    return response.data;
  }
);
export const UpdateUserAsync = createAsyncThunk(
  "user/UpdateUser",
  async (update) => {
    const response = await UpdateUser(update);
    // The value we return becomes the `fulfilled` action payload
    return response.data;
  }
);
export const orderSlice = createSlice({
  name: "user",
  initialState,
  reducers: {
    increment: (state) => {
      state.value += 1;
    },
  },
  // The `extraReducers` field lets the slice handle actions defined elsewhere,
  // including actions generated by createAsyncThunk or in other slices.
  extraReducers: (builder) => {
    builder
      .addCase(fetcLoggedInUserOrdersAsync.pending, (state) => {
        state.status = "loading";
      })
      .addCase(fetcLoggedInUserOrdersAsync.fulfilled, (state, action) => {
        state.status = "idle";
        state.userOrders = action.payload;
      })
      .addCase(fetcLoggedInUserInfoAsync.pending, (state) => {
        state.status = "loading";
      })
      .addCase(fetcLoggedInUserInfoAsync.fulfilled, (state, action) => {
        state.status = "idle";
        state.userInfo = action.payload;
      })
      .addCase(UpdateUserAsync.pending, (state) => {
        state.status = "loading";
      })
      .addCase(UpdateUserAsync.fulfilled, (state, action) => {
        state.status = "idle";
        state.userInfo = action.payload;
      });
  },
});

export const selectUserOrders = (state) => state.user.userOrders;
export const selectUserInfo = (state) => state.user.userInfo;

export default orderSlice.reducer;
